          <h4 class="fw-bold">Objectives :</h4>
          <p>With an objective of making quality generic medicines available at affordable prices to all, Pradhan Mantri Bhartiya Janaushadhi Pariyojana (PMBJP) was launched by the Department of Pharmaceuticals, Ministry of Chemicals & Fertilizers, Government of India in November, 2008. Under the scheme, dedicated outlets known as Janaushadhi Kendras are opened to provide generic medicines at affordable prices. As on 30.09.2024, 13822 Janaushadhi Kendras are functional across the country. Product basket of PMBJP comprises 2047 drugs and 300 surgical items. The Scheme is implemented by a society registered under the Societies Registration Act, viz., Pharma & Medical Bureau of India (PMBI) [erstwhile Bureau of Pharma PSUs of India (BPPI)].</p>

            <p>To make available quality medicines consumables and surgical items at affordable prices for all and reduce out of pocket expenditure of consumers/patients.</p>
            
            <p>To popularize generic medicines among the masses and dispel the prevalent notion that low priced generic medicines are of inferior quality or are less effective.</p>
            
            <p>To ensure easy availability of the menstrual health services to all women across India.</p>
            
            <p>To generate employment by engaging individual entrepreneurs in the opening of Jan Aushadhi</p> Kendra.
        </div>

        <div class="tab-pane fade" id="list-features" role="tabpanel">
          <h4 class="fw-bold">Salient Features :</h4>
          <p>The salient features of the PMBJP include cost-effective medicine availability, quality assurance, and more...</p>
            <p><b>The Scheme is operated by government agencies as well as by private entrepreneurs:</b></p>

            <p>Prices of the Jan Aushadhi medicines are 50%-80% less than that of branded medicine’s prices available in the open market.</p>
            
            <p>Medicines are procured only from World Health Organization – Good Manufacturing Practices (WHO-GMP) certified manufacturers for ensuring the quality of the products.</p>
            
            <p>Each batch of drug is tested at laboratories accredited by ‘National Accreditation Board for Testing and Calibration Laboratories’ (NABL) for ensuring best quality.</p>
            
            <p>The incentive up to Rs. 20,000/- per month is given @ 20% of monthly purchases made and subject to the stocking mandate.</p>
            
            <p>One-time incentives of Rs. 2.00 lakh is provided to the PMBJP Kendras opened in North-Eastern States, Himalayan areas, Island territories and backward areas mentioned as aspirational district by NITI Aayog or opened by women entrepreneur, Ex-serviceman Divyang, SC & ST.</p>
        </div>

        <div class="tab-pane fade" id="list-napkin" role="tabpanel">
          <h4 class="fw-bold">JanAushadhi Suvidha Sanitary Napkin :</h4>
          <p>As an important step in ensuring the health security for Indian women, Jan Aushadhi Suvidha Oxo-biodegradable Sanitary Napkins were launched on 27th August 2019 to be made available at Rs.1/- per pad only.</p>
        <p>Jan Aushadhi Suvidha Napkins are being made available for sale in more than 15000 PMBJP Kendras across the country. The cumulative sales of Suvidha Napkins as on 31.01.2025 is 72 Crores.</p>
        </div>

        <div class="tab-pane fade" id="list-app" role="tabpanel">
          <h4 class="fw-bold">Jan Aushadhi Sugam Mobile Application :</h4>
          <p>Mobile application “JanAushadhi Sugam” was launched the month of August, 2019. The App has various user-friendly features like - to locate nearby Jan Aushadhi Kendra through Google map, search Jan Aushadhi generic medicines, compare prices of Generic v/s Branded medicines in terms of MRP, overall savings, etc.</p>
        </div>

